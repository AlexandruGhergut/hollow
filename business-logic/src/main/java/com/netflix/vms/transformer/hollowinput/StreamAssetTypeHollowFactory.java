import com.netflix.hollow.read.dataaccess.HollowTypeDataAccess;
import com.netflix.hollow.read.customapi.HollowTypeAPI;

@SuppressWarnings("all")
public class StreamAssetTypeHollowFactory<T extends StreamAssetTypeHollow> extends HollowFactory<T> {

    @Override
    public T newHollowObject(HollowTypeDataAccess dataAccess, HollowTypeAPI typeAPI, int ordinal) {
        return (T)new StreamAssetTypeHollow(((StreamAssetTypeTypeAPI)typeAPI).getDelegateLookupImpl(), ordinal);
    }

    @Override
    public T newCachedHollowObject(HollowTypeDataAccess dataAccess, HollowTypeAPI typeAPI, int ordinal) {
        return (T)new StreamAssetTypeHollow(new StreamAssetTypeDelegateCachedImpl((StreamAssetTypeTypeAPI)typeAPI, ordinal), ordinal);
    }

}