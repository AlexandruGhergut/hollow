import com.netflix.hollow.read.customapi.HollowTypeAPI;

public class VideoAwardAwardHollowFactory<T extends VideoAwardAwardHollow> extends HollowFactory<T> {

    @Override
    @SuppressWarnings("unchecked")
    public T newHollowObject(HollowTypeDataAccess dataAccess, HollowTypeAPI typeAPI, int ordinal) {
        return (T)new VideoAwardAwardHollow(((VideoAwardAwardTypeAPI)typeAPI).getDelegateLookupImpl(), ordinal);
    }

    @Override
    @SuppressWarnings("unchecked")
    public T newCachedHollowObject(HollowTypeDataAccess dataAccess, HollowTypeAPI typeAPI, int ordinal) {
        return (T)new VideoAwardAwardHollow(new VideoAwardAwardDelegateCachedImpl((VideoAwardAwardTypeAPI)typeAPI, ordinal), ordinal);
    }

}